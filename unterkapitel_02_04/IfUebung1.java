package unterkapitel_02_04;

public class IfUebung1 {

    public static void main(String[] args) {
        
        int zahl = 27;

        if (zahl % 2 == 0) {
            System.out.println("Die zahl " + zahl + " ist gerade");
        } 

        if (zahl % 2 != 0){
            System.out.println("Die zahl " + zahl + " ist ungerade");
        }
        // else { System.out.println("Die Zahl ist ungerade");}

        
    }

}
